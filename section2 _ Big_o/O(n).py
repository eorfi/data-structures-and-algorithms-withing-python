def print_items(n):
    for i in range(n):
        print(i)

print_items(10)

# Time Complexity: O(n)
# Space Complexity: O(1)
# Explanation: The function iterates through a single loop from 0 to n-1, printing each value of i.
# The time complexity is linear, O(n), because the number of iterations grows linearly with n.
# The space complexity is O(1) since no additional data structures are used that grow with n.
# This means that the function's memory usage does not depend on the size of n.
# The function is efficient for large values of n, as it only requires a single loop and constant space.
# The function is simple and straightforward, making it easy to understand and maintain.
# The function can be used in various applications where a simple iteration over a range is needed.
              
                  
                  






