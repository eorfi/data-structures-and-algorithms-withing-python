def add_items(n):
    return n + n + n
# the o(1) is the time complexity of this function because it does not depend on the size of n.
# The function simply returns the result of adding n three times, which is a constant time operation.
# The space complexity is also O(1) because it does not use any additional data structures that grow with n.
# This means that the function's memory usage does not depend on the size of n.
# The function is efficient and performs the addition operation in constant time.
# This makes it suitable for scenarios where performance is critical and the input size can vary.
# The function is simple and easy to understand, making it a good choice for basic arithmetic operations.
# The function can be used in various applications where a constant time addition is needed.
# The function is not optimized for performance, but it is already efficient due to its constant time complexity.
# The function is straightforward and does not involve any complex logic or iterations.
# The function is useful for understanding how constant time operations work in terms of time complexity.
 
print(add_items(10))